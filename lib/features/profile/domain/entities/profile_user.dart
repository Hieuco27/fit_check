class ProfileUser {
  final String name;
  final String styleTitle;
  final String location;
  final String avatarUrl;
  final int outfitsSaved;
  final int stylesAnalyzed;
  final String scoreTitle;
  final String scoreSubtitle;

  const ProfileUser({
    required this.name,
    required this.styleTitle,
    required this.location,
    required this.avatarUrl,
    required this.outfitsSaved,
    required this.stylesAnalyzed,
    required this.scoreTitle,
    required this.scoreSubtitle,
  });
}
